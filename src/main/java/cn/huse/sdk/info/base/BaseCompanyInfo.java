package cn.huse.sdk.info.base;

/**
 * @author: huanxi
 * @date: 2019-06-11 17:35
 */
public class BaseCompanyInfo {
    protected String name;        //公司名
    protected String registeredCapital;
    protected String taxpayerRegistration;
    protected String dateOfEstablishment;
    protected String businessRegistrationNumber;
    protected String registrationAuthority;
    protected String registeredAddress;
    protected String scope;
    protected String dateOfApproval;
    protected String image;

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRegisteredCapital() {
        return registeredCapital;
    }

    public void setRegisteredCapital(String registeredCapital) {
        this.registeredCapital = registeredCapital;
    }

    public String getTaxpayerRegistration() {
        return taxpayerRegistration;
    }

    public void setTaxpayerRegistration(String taxpayerRegistration) {
        this.taxpayerRegistration = taxpayerRegistration;
    }

    public String getDateOfEstablishment() {
        return dateOfEstablishment;
    }

    public void setDateOfEstablishment(String dateOfEstablishment) {
        this.dateOfEstablishment = dateOfEstablishment;
    }

    public String getBusinessRegistrationNumber() {
        return businessRegistrationNumber;
    }

    public void setBusinessRegistrationNumber(String businessRegistrationNumber) {
        this.businessRegistrationNumber = businessRegistrationNumber;
    }

    public String getRegistrationAuthority() {
        return registrationAuthority;
    }

    public void setRegistrationAuthority(String registrationAuthority) {
        this.registrationAuthority = registrationAuthority;
    }

    public String getRegisteredAddress() {
        return registeredAddress;
    }

    public void setRegisteredAddress(String registeredAddress) {
        this.registeredAddress = registeredAddress;
    }

    public String getScope() {
        return scope;
    }

    public void setScope(String scope) {
        this.scope = scope;
    }

    public String getDateOfApproval() {
        return dateOfApproval;
    }

    public void setDateOfApproval(String dateOfApproval) {
        this.dateOfApproval = dateOfApproval;
    }
}
